@RestController
public class MqController {
    @Autowired
    private RabbitTemplate template;

    @GetMapping("/api/mq/sendmsg/{msg}")
    public String send(@PathVariable String msg){
        //发送消息
        template.convertAndSend("fly",msg);

        return "吃饭->" + System.currentTimeMillis();
    }
}
